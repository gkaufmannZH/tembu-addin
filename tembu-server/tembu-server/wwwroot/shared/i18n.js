  // ── Lookup helpers ────────────────────────────────────────────────────
  function resolve(dictForLang, key) {
    const parts = key.split('.');
    let node = dictForLang;
    for (const p of parts) {
      if (node == null) return undefined;
      node = node[p];
    }
    return node;
  }

  function interpolate(str, vars) {
    if (!vars) return str;
    return str.replace(/\{(\w+)\}/g, (m, name) => (name in vars ? vars[name] : m));
  }

  function t(key, vars) {
    const lang = getLang();
    let val = resolve(DICT[lang], key);
    if (val === undefined) val = resolve(DICT[DEFAULT], key);
    if (val === undefined) return key;
    if (typeof val !== 'string') return key; // caller used t() on a plural key by mistake
    return interpolate(val, vars);
  }

  function tn(key, count, vars) {
    const lang = getLang();
    let val = resolve(DICT[lang], key);
    if (val === undefined) val = resolve(DICT[DEFAULT], key);
    if (val === undefined) return key;
    let category = 'other';
    try { category = new Intl.PluralRules(lang).select(count); } catch {}
    const str = (val && val[category] !== undefined) ? val[category] : (val.other !== undefined ? val.other : key);
    return interpolate(str, Object.assign({ n: count }, vars || {}));
  }

  function formatDate(date, opts) {
    return date.toLocaleDateString(DATE_LOCALE_MAP[getLang()] || 'en-US', opts);
  }

  function formatNumber(n) {
    return n.toLocaleString(DATE_LOCALE_MAP[getLang()] || 'en-US');
  }

  function sinceLabel(months) {
    if (months === 0) return t('common.sinceAll');
    if (months < 12) return tn('common.sinceMonths', months);
    if (months === 12) return t('common.sinceYear');
    return tn('common.sinceYears', Math.round(months / 12));
  }

  function applyStaticI18n(root) {
    root = root || document;
    root.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = t(key);
      if (el.hasAttribute('data-i18n-html')) el.innerHTML = val;
      else el.textContent = val;
    });
    root.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    root.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = t(el.getAttribute('data-i18n-title'));
    });
    document.documentElement.lang = getLang();
  }

  return {
    LANG_KEY,
    SUPPORTED,
    getLang,
    setLang,
    t,
    tn,
    formatDate,
    formatNumber,
    sinceLabel,
    applyStaticI18n,
  };
